     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = Reporte::all();

        $data = new Reporte;
        $data->correo_reporte = $request->email;
        $data->asunto_reporte = $request->asunto;
        $data->descripcion_reporte = $request->descripcion;
        $data->revisado_reporte = 3;
        $data->save();
        return redirect()->route('contacto')->with('success', 'Reporte creado correctamente.');

    }

